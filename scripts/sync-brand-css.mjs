#!/usr/bin/env node
/**
 * Generates `src/styles/brand-theme.css` from `src/brand/tokens.json`.
 * Run via `npm run sync:brand` (also runs before `dev` / `build`).
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const tokensPath = join(root, 'src/brand/tokens.json')
const outPath = join(root, 'src/styles/brand-theme.css')

const { colors } = JSON.parse(readFileSync(tokensPath, 'utf8'))

const lines = Object.entries(colors).map(([key, value]) => `  --color-${key}: ${value};`)

const css = `/* Generated by scripts/sync-brand-css.mjs — edit src/brand/tokens.json instead */

@theme {
${lines.join('\n')}
}
`

writeFileSync(outPath, css, 'utf8')
console.log('sync:brand →', outPath)
